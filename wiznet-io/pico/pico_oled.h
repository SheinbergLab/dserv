/*
 * pico_oled.h -- SSD1306 128x32 SPI status display (e.g. Adafruit 661).
 *
 * Driven from CORE 0 ONLY: a status panel is exactly the class of slow,
 * cosmetic peripheral the dual-core split parks on the housekeeping core.
 * Core 1 keeps a small snapshot current (single-writer volatiles in the main
 * .c); core 0 renders it a few times a second. A full frame is 512 bytes at
 * 8 MHz SPI ~= 0.5 ms, blocking, on core 0 -- invisible to the RT core, and
 * on a DEDICATED bus (SPI0), so unlike an I2C panel it can never delay an
 * ADS1115 sample.
 *
 * Runtime-enabled via `oled enable 1` + save/reboot (the ain_en pattern).
 * SSD1306-over-SPI is write-only (no MISO), so there is nothing to probe and
 * no failure to trip: with no display attached the writes go nowhere,
 * harmlessly. Pins (OLED_PIN_* in pico_gpio.h, reserved while enabled):
 * SPI0 SCK=GP2, MOSI=GP3, CS=GP6, DC=GP7, RST=GP8 -- see wiznet-io/PINMAP.md.
 *
 * Text grid: 21 cols x 4 rows (5x7 glyphs in 6x8 cells).
 */
