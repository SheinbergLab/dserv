static inline void oled_cmd(const uint8_t *c, int n)
{
    gpio_put(OLED_PIN_DC, 0);
    gpio_put(OLED_PIN_CS, 0);
    spi_write_blocking(OLED_SPI, c, (size_t) n);
    gpio_put(OLED_PIN_CS, 1);
}

static inline void oled_clear(void) { memset(oled_fb, 0, sizeof oled_fb); }

/* Write a string at (col,row) in the 21x4 grid; clips at the right edge. */
static inline void oled_text(int col, int row, const char *s)
{
    if (row < 0 || row >= OLED_ROWS || col < 0) return;
    uint8_t *p = &oled_fb[row * OLED_W + col * 6];
    while (*s && col < OLED_COLS) {
        int ch = (uint8_t) *s++;
        if (ch < 0x20 || ch > 0x7E) ch = '?';
        memcpy(p, oled_font[ch - 0x20], 5);
        p[5] = 0;                              /* 1px inter-glyph gap */
        p += 6;
        col++;
    }
}

/* Push the whole framebuffer (512B @ 8MHz ~= 0.5ms, blocking, core 0). */
static inline void oled_flush(void)
{
    if (!oled_on) return;
    static const uint8_t addr[] = { 0x21, 0, OLED_W - 1,        /* column window   */
                                    0x22, 0, OLED_PAGES - 1 };  /* page window     */
    oled_cmd(addr, sizeof addr);
    gpio_put(OLED_PIN_DC, 1);
    gpio_put(OLED_PIN_CS, 0);
    spi_write_blocking(OLED_SPI, oled_fb, sizeof oled_fb);
    gpio_put(OLED_PIN_CS, 1);
}

static inline void oled_init(void)
{
    spi_init(OLED_SPI, OLED_BAUD);
    gpio_set_function(OLED_PIN_SCK,  GPIO_FUNC_SPI);
    gpio_set_function(OLED_PIN_MOSI, GPIO_FUNC_SPI);
    gpio_init(OLED_PIN_CS);  gpio_set_dir(OLED_PIN_CS,  GPIO_OUT); gpio_put(OLED_PIN_CS, 1);
    gpio_init(OLED_PIN_DC);  gpio_set_dir(OLED_PIN_DC,  GPIO_OUT); gpio_put(OLED_PIN_DC, 0);
    gpio_init(OLED_PIN_RST); gpio_set_dir(OLED_PIN_RST, GPIO_OUT);
    gpio_put(OLED_PIN_RST, 1); sleep_ms(1);     /* boot-time only: sleeps are fine here */
    gpio_put(OLED_PIN_RST, 0); sleep_ms(10);
    gpio_put(OLED_PIN_RST, 1); sleep_ms(10);

    static const uint8_t seq[] = {
        0xAE,                   /* display off                     */
        0xD5, 0x80,             /* clock divide ratio              */
        0xA8, 0x1F,             /* multiplex 31 -> 128x32          */
        0xD3, 0x00,             /* display offset 0                */
        0x40,                   /* start line 0                    */
        0x8D, 0x14,             /* charge pump on (no ext Vcc)     */
        0x20, 0x00,             /* horizontal addressing mode      */
        0xA1,                   /* segment remap                   */
        0xC8,                   /* COM scan direction (flip)       */
        0xDA, 0x02,             /* COM pins: sequential (128x32)   */
        0x81, 0x8F,             /* contrast                        */
        0xD9, 0xF1,             /* precharge                       */
        0xDB, 0x40,             /* VCOMH deselect                  */
        0xA4,                   /* display follows RAM             */
        0xA6,                   /* normal (not inverted)           */
        0xAF,                   /* display ON                      */
    };
    oled_on = 1;
    oled_cmd(seq, sizeof seq);
    oled_clear();
    oled_text(3, 1, "extio booting..");
    oled_flush();
    printf("oled: SSD1306 128x32 SPI0 (SCK%d MOSI%d CS%d DC%d RST%d)\n",
           OLED_PIN_SCK, OLED_PIN_MOSI, OLED_PIN_CS, OLED_PIN_DC, OLED_PIN_RST);
}

